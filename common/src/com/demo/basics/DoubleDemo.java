package com.demo.basics;

public class DoubleDemo {

	public static void main(String[] args) {
		//decimal number format are as follows:

			double   d1  = 8D ;
			double   d2  = 8.; 
			double   d3  =  8.0; 
			double   d4  =  8.D; 
			double   d5  =  78.9867; 
			double   d6  =  45.0;
			
			//We can also use scientific notation to express double literals.

			double   d01  = 32.5E-1; 
			double   d02  = 0.325E+1; 
			double   d03  = 0.325E1; 
			double   d04  = 0.0325E2; 
			double   d05  = 0.0325e2; 
			double   d06  = 32.5E-1D; 
			double   d07  = 0.325E+1d; 
			double   d08  = 0.325E1d; 
			double   d09  = 0.0325E2d;
	}

}
