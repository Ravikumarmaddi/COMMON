package com.demo.basics;

public class IntDemo {
	public static void main(String[] args) {
		// Decimal number format
		int num1 = 17;
		
		// Octal number format
		int num81 = 021;
		
		// Hexadecimal number format
		int num01 = 0x123;
		int num02 = 0xdecafe;
		int num03 = 0x1A2B;
		int num04 = 0X0123;
		
		// Binary number format
		int numb1 = 0b10101;
		int numb2 = 0b00011;
		int numb3 = 0b10;
		int numb4 = 0b00000010;
		
		//-32768 to 32767 (or -2^15 to 2^15 - 1).
		short s1  = 11111;      // ok 
		short s2  = -11111;   // ok

	}

}
